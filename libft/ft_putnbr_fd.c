#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	i;

	if (n > INT_MAX || n < INT_MIN)
		return ;
	if (n == -2147483648)
	{
		ft_putchar_fd('-', fd);
		ft_putchar_fd('2', fd);
		n = 147483648;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	i = (n % 10) + '0';
	ft_putchar_fd(i, fd);
}

